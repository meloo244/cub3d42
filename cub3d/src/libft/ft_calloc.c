#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*str_alloc;

	if (size && count >= (SIZE_MAX / size))
		return (0);
	str_alloc = malloc(count * size);
	if (!str_alloc)
		return ((void *)0);
	ft_bzero(str_alloc, count * size);
	return (str_alloc);
}
