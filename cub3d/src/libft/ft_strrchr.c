#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	str_len;

	str_len = (int)ft_strlen(s);
	while (str_len >= 0)
	{
		if (s[str_len] == (char)c)
			return ((char *)s + str_len);
		str_len--;
	}
	return (0);
}
